// Comparison in JavaScript

// 1. Equality (==) - checks if values are equal, ignores data type
console.log(5 == '5'); // true - example: comparing a number and a string

// 2. Strict Equality (===) - checks if values and data types are equal
console.log(5 === '5'); // false - example: comparing a number and a string with strict equality

// 3. Inequality (!=) - checks if values are not equal, ignores data type
console.log(5 != '5'); // false - example: comparing a number and a string for inequality

// 4. Strict Inequality (!==) - checks if values or data types are not equal
console.log(5 !== '5'); // true - example: comparing a number and a string with strict inequality

// 5. Greater Than (>) - checks if a value is greater than another
console.log(5 > 3); // true - example: comparing numbers

// 6. Less Than (<) - checks if a value is less than another
console.log(5 < 3); // false - example: comparing numbers

// 7. Greater Than or Equal To (>=) - checks if a value is greater than or equal to another
console.log(5 >= 5); // true - example: comparing numbers

// 8. Less Than or Equal To (<=) - checks if a value is less than or equal to another
console.log(5 <= 5); // true - example: comparing numbers
