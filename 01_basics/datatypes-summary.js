

// Primitive Data Types

// 1. Number: represents a numeric value
let num = 10; // example: assigning a numeric value to a variable

// 2. String: represents a sequence of characters
let str = 'hello'; // example: assigning a string value to a variable

// 3. Boolean: represents a true or false value
let bool = true; // example: assigning a boolean value to a variable

// 4. Null: represents the absence of any object value
let nullValue = null; // example: assigning null to a variable

// 5. Undefined: represents an uninitialized or non-existent variable
let undef; // example: declaring a variable without assigning a value

// 6. Symbol: represents a unique identifier (introduced in ES6)
let sym = Symbol('unique'); // example: creating a symbol

// Non-Primitive Data Types

// 1. Object: represents a collection of key-value pairs
let obj = { name: 'John', age: 30 }; // example: creating an object

// 2. Array: represents an ordered collection of values
let arr = [1, 2, 3, 4, 5]; // example: creating an array

// 3. Function: represents a block of code that can be executed
let func = function() { console.log('Hello!'); }; // example: creating a function

